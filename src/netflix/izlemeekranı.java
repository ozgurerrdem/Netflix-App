    private void bolumyukle() {
        conn = Sqlbaglanti.getConnection();
        try {
            st = conn.createStatement();
            rs = st.executeQuery("SELECT * FROM kullanici_program");
            while (rs.next()) {
                if (rs.getString("mail").equalsIgnoreCase(girisekrani.mail) && rs.getString("programadi").equalsIgnoreCase(anasayfa.izlenecekfilm)) {
                    if (rs.getInt("kaldıgıbolum") != 0) {
                        jLabel4.setText(rs.getInt("kaldıgıbolum") + ". bölümde kalmıştınız.");
                    }
                    puanla.setSelectedIndex(rs.getInt("puan") - 1);

                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(izlemeekranı.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

