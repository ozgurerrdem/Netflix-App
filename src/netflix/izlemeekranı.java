    private void playActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_playActionPerformed
        durum.setText("Oynatılıyor");
        conn = Sqlbaglanti.getConnection();
        try {
            st = conn.createStatement();
            rs = st.executeQuery("select kaldıgıdk,kaldıgısn,mail,programadi FROM kullanici_program");
            while (rs.next()) {
                if (rs.getString("mail").equalsIgnoreCase(girisekrani.mail) && rs.getString("programadi").equalsIgnoreCase(anasayfa.izlenecekfilm)) {
                    gecendakika = rs.getInt("kaldıgıdk");
                    gecensaniye = rs.getInt("kaldıgısn");
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(izlemeekranı.class.getName()).log(Level.SEVERE, null, ex);
        }

        t = new Timer(1000, (ActionEvent e) -> {
            if (gecensaniye > 59) {
                gecendakika++;
                gecensaniye /= 60;
                gecensaniye--;
            }
            sure.setText(String.valueOf(gecendakika) + ":" + String.valueOf(gecensaniye));
            gecensaniye++;
        });
        t.start();
    }//GEN-LAST:event_playActionPerformed

